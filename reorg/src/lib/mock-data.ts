function storeVal(
  platform: Platform,
  listingId: string,
  value: number | string | null,
  opts?: { stagedValue?: number | string | null; url?: string; variantId?: string }
) {
  return {
    platform,
    listingId,
    value,
    variantId: opts?.variantId,
    stagedValue: opts?.stagedValue,
    url: opts?.url ?? `https://www.ebay.com/itm/${listingId}`,
  };
}

function profit(sale: number, supplierCost: number, supplierShipping: number, shippingCost: number, feeRate: number, adRate: number): number {
  const fee = sale * feeRate;
  const ad = sale * adRate;
  return Math.round((sale - supplierCost - supplierShipping - shippingCost - fee - ad) * 100) / 100;
}

